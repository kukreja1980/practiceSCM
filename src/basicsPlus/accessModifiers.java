package basicsPlus;

public class accessModifiers {

	int var1;
	private int var2;
	public int var3;
	protected int var4;
	
	void fuction1() {
		System.out.println("This is no access modifier fn");
	}
	
	private void function2() {
		System.out.println("This is private fn");
	}
	
	public void fuction3() {
		System.out.println("This is public fn");
	}
	
	protected void fuction4() {
		System.out.println("This is protected fn");
	}
}
